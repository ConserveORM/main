package org.conserve.objects;

/**
 * @author Erik Berglund
 *
 */
public class ObjectArrayContainingObject
{
	private Object [] data;

	/**
	 * 
	 */
	public ObjectArrayContainingObject()
	{
	}

	/**
	 * @return the data
	 */
	public Object [] getData()
	{
		return data;
	}

	/**
	 * @param data the data to set
	 */
	public void setData(Object [] data)
	{
		this.data = data;
	}

}
